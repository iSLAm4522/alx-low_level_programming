/**
 * reset_to_98 - Updates the value of the integer pointed to
 * by the parameter to 98.
 * @n: Pointer to an integer that will be updated.
 *
 * Description: This function takes a pointer to an integer as an argument
 * and updates the value of the integer it points to to 98.
 */

void reset_to_98(int *n)
{
*n = 98;
}
